function deepMergeDict(base: TranslationDict, overlay: TranslationDict): TranslationDict {
  const result: TranslationDict = { ...base }
  for (const [key, value] of Object.entries(overlay)) {
    const baseValue = base[key]
    if (typeof value === 'string') {
      result[key] = value
    } else if (value && typeof value === 'object') {
      result[key] = deepMergeDict(
        typeof baseValue === 'object' && baseValue ? (baseValue as TranslationDict) : {},
        value as TranslationDict,
      )
    }
  }
  return result
}

export const translations: Record<Locale, Record<string, string>> = {
  fr: flatten(fr),
  en: flatten(deepMergeDict(fr, en)),
  ln: flatten(deepMergeDict(fr, ln)),
  lua: flatten(deepMergeDict(fr, lua)),
  kg: flatten(deepMergeDict(fr, kg)),
  sw: flatten(deepMergeDict(fr, sw)),
}

export function translate(locale: Locale, key: string): string {
  return translations[locale][key] ?? translations.fr[key] ?? key
}
